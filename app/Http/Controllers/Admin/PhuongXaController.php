     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        try {
			$limit = $request->get('limit', 25);
			$ascending = (int)$request->get('ascending', 0);
			$orderBy = $request->get('orderBy', '');
			$search = $request->get('search', '');
			$betweenDate = $request->get('updated_at', []);

			$queryService = new QueryService(new PhuongXa);
            $queryService->select = [];
            $queryService->columnSearch = ['tenPhuongXa'];
            $queryService->withRelationship = ['huyen'];
            $queryService->search = $search;
            $queryService->betweenDate = $betweenDate;
            $queryService->limit = $limit;
            $queryService->ascending = $ascending;
            $queryService->orderBy = $orderBy;

            $query = $queryService->queryTable();
            $query = $query->paginate($limit);
            $phuongXa = $query->toArray();

			return $this->jsonTable($phuongXa);
		} catch (\Exception $e) {
			return $this->jsonError($e);
		}
    }

